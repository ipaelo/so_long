#include "libft.h"

char	*ft_strjoin2(char const *s1, char const *s2)
{
	size_t	i;
	size_t	c;
	char	*str;

	if (!s2)
		return ((char *)s1);
	str = (char *)malloc((ft_strlen(s1) + ft_strlen(s2) + 1) * sizeof(char));
	if (!str)
		return (NULL);
	i = -1;
	c = 0;
	if (s1)
		while (s1[++i] != '\0')
			str[i] = s1[i];
	while (s2[c] != '\0')
		str[i++] = s2[c++];
	str[ft_strlen(s1) + ft_strlen(s2)] = '\0';
	free((void *)s1);
	return (str);
}
